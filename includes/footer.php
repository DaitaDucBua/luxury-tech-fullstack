    // Global SITE_URL for JavaScript
    const SITE_URL = '<?php echo SITE_URL; ?>';

    // Function to update user info in header
    function updateUserInfo() {
        fetch(SITE_URL + '/ajax/get-user-info.php?t=' + Date.now())
            .then(response => response.json())
            .then(data => {
                const container = document.getElementById('user-info-container');
                if (!container) return;

                if (data.logged_in) {
                    // User is logged in
                    let html = '<span class="text-primary font-medium flex items-center gap-1">';
                    html += '<i class="fas fa-user-circle"></i>';
                    html += escapeHtml(data.user.full_name);
                    html += '</span>';
                    html += '<span class="text-gray-300">|</span>';

                    if (data.user.role === 'admin') {
                        html += '<a href="<?php echo SITE_URL; ?>/admin/" class="text-gray-600 hover:text-primary transition-colors flex items-center gap-1"><i class="fas fa-shield-alt"></i>Admin</a>';
                        html += '<span class="text-gray-300">|</span>';
                    }

                    html += '<a href="<?php echo SITE_URL; ?>/profile.php" class="text-gray-600 hover:text-primary transition-colors">Tài khoản</a>';
                    html += '<span class="text-gray-300">|</span>';
                    html += '<a href="<?php echo SITE_URL; ?>/orders.php" class="text-gray-600 hover:text-primary transition-colors">Đơn hàng</a>';
                    html += '<span class="text-gray-300">|</span>';
                    html += '<a href="<?php echo SITE_URL; ?>/logout.php" class="text-gray-600 hover:text-primary transition-colors" onclick="return handleLogout(event)">Đăng xuất</a>';

                    container.innerHTML = html;
                    // Force repaint
                    container.style.display = 'none';
                    container.offsetHeight; // Trigger reflow
                    container.style.display = 'flex';
                } else {
                    // User is not logged in
                    container.innerHTML = '<a href="<?php echo SITE_URL; ?>/login.php" class="text-gray-600 hover:text-primary transition-colors flex items-center gap-1"><i class="fas fa-sign-in-alt"></i> Đăng nhập</a>' +
                        '<span class="text-gray-300">|</span>' +
                        '<a href="<?php echo SITE_URL; ?>/register.php" class="text-primary font-medium hover:text-primary-dark transition-colors flex items-center gap-1"><i class="fas fa-user-plus"></i> Đăng ký</a>';
                    // Force repaint
                    container.style.display = 'none';
                    container.offsetHeight; // Trigger reflow
                    container.style.display = 'flex';
                }

                // Update mini cart too
                if (typeof updateMiniCart === 'function') {
                    updateMiniCart();
                }
            })
            .catch(error => console.error('Error updating user info:', error));
    }

    // Helper function to escape HTML
    function escapeHtml(text) {
        const div = document.createElement('div');
        div.textContent = text;
        return div.innerHTML;
    }

    // Handle logout with AJAX update
    function handleLogout(event) {
        event.preventDefault();

        // Redirect to logout
        window.location.href = '<?php echo SITE_URL; ?>/logout.php';

        return false;
    }

